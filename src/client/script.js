/**
 * „Ç®„Éá„Ç£„Çø„ÅÆ„Çø„Ç§„Éà„É´ÊñáÂ≠óÂàó„Å®„Å™„ÇãÈÖçÂàó„ÇíÁîüÊàê„Åó„Å¶Ëøî„Åô
 */
function getTitleArray(data){
    let titles = Object.keys(data);
    titles.sort();
    let frag = [];
    titles.forEach((v, index) => {
        if(v.includes('fs') === true){
            frag.push(v);
        }
    });
    if(frag.length > 0){
        titles.splice(0, frag.length);
        frag.forEach((v, index) => {
            let i = 2 + index * 2 + 1;
            titles.splice(i, 0, v);
        });
    }
    return titles;
}

/**
 * @return {Promise}
 */
function editorSetting(data){
    let titles = getTitleArray(data);
    if(editors != null){
        editors.forEach((v) => {
            v = null;
        });
    }
    editors = [];
    return new Promise((resolve, reject) => {
        // „Çø„Éñ„ÅÆÂêÑ„Éö„Éº„Ç∏„Å´„Ç®„Éá„Ç£„Çø„ÇíÈÖçÁΩÆ„ÅóÂàùÊúüÂåñ„Åô„Çã
        pages.forEach((v, index) => {
            let type = '';
            switch(true){
                case titles[index].includes('html'):
                    type = 'html';
                    break;
                case titles[index].includes('js'):
                    type = 'javascript';
                    break;
                case titles[index].includes('vs'):
                case titles[index].includes('fs'):
                    type = 'glsl';
                    break;
                default:
                    reject('invalid type');
                    return;
            }
            let editor = ace.edit(v.id);
            editor.$blockScrolling = Infinity;
            editor.setOptions(EDITOR_OPTION);
            editor.session.setMode(`ace/mode/${type}`);
            editor.session.setUseWrapMode(true);
            editor.session.setTabSize(4);

            // event setting
            let vimMode = false;
            // Ë´∏‰∫ãÊÉÖ„Å´„Çà„Çä Command + L „ÅØÂ∞ÅÂç∞„Åô„Çã
            editor.commands.addCommand({
                name: 'disableCtrl-L',
                bindKey: {win: 'Ctrl-L', mac: 'Command-L'},
                exec: () => {},
            });
            // vim „ÅÆ„Ç≠„Éº„Éê„Ç§„É≥„Éâ„Å´Â§âÊõ¥
            editor.commands.addCommand({
                name: 'toggleVimMode',
                bindKey: {win: 'Ctrl-Alt-V', mac: 'Command-Alt-V'},
                exec: () => {
                    if(vimMode !== true){
                        editor.setKeyboardHandler('ace/keyboard/vim');
                    }else{
                        editor.setKeyboardHandler(null);
                    }
                    vimMode = !vimMode;
                },
            });

            // Â§âÊõ¥„Åå„ÅÇ„Å£„Åü„Åì„Å®„ÇíÊ§úÂá∫„Åó„Å¶Â∑¶„Çµ„Ç§„Éâ„Éê„Éº„ÅÆ„É™„Çπ„Éà‰∏ä„Å´„Ç§„É≥„Ç∏„Ç±„Éº„Çø„ÇíÂá∫„Åô„Åü„ÇÅ„ÅÆÂá¶ÁêÜ
            editor.session.on('change', () => {
                if(isGeneration === true){return;}
                if(latestResponse != null && latestActive != null && latestResponse.dirs[latestActive] != null){
                    latestResponse.dirs[latestActive].changes = true;
                    items[latestActive].update(null, true)
                }
            });

            editors.push(editor);
        });

        resolve();
    });
}

